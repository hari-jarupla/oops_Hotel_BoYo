    private void jDatePicker2ActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // TODO add your handling code here:
        int month = jDatePicker1.getModel().getMonth()-jDatePicker2.getModel().getMonth();
       
        int dayStrt = jDatePicker1.getModel().getDay();
        int dayEnd = jDatePicker2.getModel().getDay();
        int monthStrt = jDatePicker1.getModel().getMonth();
        int monthEnd = jDatePicker2.getModel().getMonth();
        int yearStrt = jDatePicker1.getModel().getYear();
        int yearEnd = jDatePicker2.getModel().getYear();
        
        if(month == 0){
            dayStay = dayStrt-dayEnd;
            if(dayStay < 0){
                dayStay = dayStay*(-1);
            }
        }
        else{
                if(monthStrt%2 == 0){
                    dayStrt = 30-dayStrt;
                }
                else if(monthStrt%2 != 0){
                    dayStrt = 31-dayStrt;
                }
                dayStay = dayStrt+dayEnd;
            
        }
        
            dayStay = dayStay + 1;
            jLabel4.setText(Integer.toString(dayStay));
            
            from = Integer.toString(dayStrt)+"/"+Integer.toString(monthStrt)+"/"+Integer.toString(yearStrt);
            to = Integer.toString(dayEnd)+"/"+Integer.toString(monthEnd)+"/"+Integer.toString(yearEnd);

    }                                            

