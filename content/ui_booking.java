    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {
        // TODO add your handling code here:
        int sum = 0;
        int hall = 0, room = 0;
        String roomType = "";
        String roomID = String.valueOf(System.currentTimeMillis()%1000);
        String numGuest = "";
        
        //cab
        if(jCheckBox1.isSelected()){
          sum = sum + 1000;
        }
        if(jCheckBox2.isSelected()){
            sum = sum + 5000;
        }
        
        //hall
        if(jRadioButton5.isSelected()){
            hall = 10000;
        }
        if(jRadioButton6.isSelected()){
            hall = 5000;
        }
        if(jRadioButton7.isSelected()){
            hall = 8000;
        }
        if(jRadioButton8.isSelected()){
            hall = 12000;
        }
        
        //room
        if(jRadioButton1.isSelected()){
            numGuest = "2";
            roomType = "pair";
            room = 7500;
        }
        if(jRadioButton2.isSelected()){
            numGuest = "4";
            roomType = "family";
            room = 9000;
        }
        if(jRadioButton3.isSelected()){
            numGuest = "6";
            roomType = "group";
            room = 15500;
        }
        if(jRadioButton4.isSelected()){
            numGuest = "1";
            roomType = "business";
            room = 3500;
        }
        
        
        sum = sum + hall + (room*dayStay);

        SqlStatementManager sqlHelp = new SqlStatementManager("boyodb", "root", "Yashesh#07");
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection connection = sqlHelp.getConnection();
        String q = "INSERT INTO roomtable(roomcost, roomtype, roomno, numGuest, UserID) VALUES(?, ?, ?, ?, ?)";
        PreparedStatement ps = connection.prepareStatement(q);
        ps.setString(1, String.valueOf(sum));
        ps.setString(2, roomType);
        ps.setString(3, roomID);
        ps.setString(4, numGuest);
        ps.setString(5, CurrentUser.ID);
        ps.executeUpdate();
        ps.close();

        this.setVisible(false);
        this.dispose();

    }                                        

